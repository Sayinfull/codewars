package ru.sayinfull.education.kyu8;


public class ConvertBooleanToString {
    public static void main(String[] args) {
        System.out.println(convert(false));
    }
    public static String convert(boolean b) {
        if (true) {
            return String.valueOf(b);
        } else {
            return String.valueOf(b);
        }
    }
}
